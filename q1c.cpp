/*C program to perform all the arithmetic operations*/
#include <stdio.h>
#include <math.h>

int main()
{
int a,b;
printf("Enter the two numbers:- \n");
scanf("%d%d",&a,&b);
printf("The value of sum is %d \n", a+b);
printf("The value of difference is %d \n", a-b);
printf("The value of their product is %d \n", a*b);
printf("The value of their division is %d", a/b);
return 0;
}
